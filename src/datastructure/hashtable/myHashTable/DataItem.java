package datastructure.hashtable.myHashTable;
class DataItem { 
	private int key;
	private String value;

	// --------------------------------------------------------------
	public DataItem(int ii, String v) // constructor
	{
		key = ii;
		value = v;
	}

	// --------------------------------------------------------------
	public int getKey() {
		return key;
	}

	public String getValue() {
		return value;
	}
	// --------------------------------------------------------------
} // end class DataItem
